<script>
  $(document).ready(function(){
    // Setup datatables
    $.fn.dataTableExt.oApi.fnPagingInfo = function(oSettings)
      {
          return {
              "iStart": oSettings._iDisplayStart,
              "iEnd": oSettings.fnDisplayEnd(),
              "iLength": oSettings._iDisplayLength,
              "iTotal": oSettings.fnRecordsTotal(),
              "iFilteredTotal": oSettings.fnRecordsDisplay(),
              "iPage": Math.ceil(oSettings._iDisplayStart / oSettings._iDisplayLength),
              "iTotalPages": Math.ceil(oSettings.fnRecordsDisplay() / oSettings._iDisplayLength)
          };
      };

      var table = $("#mytable").dataTable({
          initComplete: function() {
              var api = this.api();
              $('#mytable_filter keyup')
                  .off('.DT')
                  .on('keyup.DT', function() {
                      api.search(this.value).draw();
              });
          },
              oLanguage: {
              sProcessing: "loading..."
          },
              scrollX: true,
              processing: true,
              serverSide: true,
              ajax: {"url": "<?php echo base_url()?>/admin/Container/get_otw_json", "type": "POST"},
                  columns: [
                        {"data": "kode"},
                        {"data": "nomor"},
                        {"data": "tanggal_berangkat_c"},
                        {"data": "view"}
                  ],
              order: [[2, 'desc']],
          rowCallback: function(row, data, iDisplayIndex) {
              var info = this.fnPagingInfo();
              var page = info.iPage;
              var length = info.iLength;
              $('td:eq(0)', row).html();
          }

      });
      // end setup datatables
      // get Edit Records
      $('#mytable').on('click','.edit_record',function(){
            var id_ekspedisi=$(this).data('id_ekspedisi');
            var nama_ekspedisi=$(this).data('nama_ekspedisi');
            var alamat=$(this).data('alamat');
            var no_telp=$(this).data('no_telp');


            $('#ModalUpdate').modal('show');
            $('[name="id_ekspedisi"]').val(id_ekspedisi);
            $('[name="nama_ekspedisi"]').val(nama_ekspedisi);
            $('[name="alamat"]').val(alamat);
            $('[name="no_telp"]').val(no_telp);

      });
      // End Edit Records


  });
</script>
